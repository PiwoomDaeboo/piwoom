import { useQuery } from '@tanstack/react-query'

import { AxiosError } from 'axios'

import instance from '@/configs/axios/instance'

import { CommonErrorType } from '../@types/data-contracts'
import {
  Parameter,
  QueryHookParams,
  RequestFnReturn,
} from '../@types/react-query-type'
import { CompanyApi } from './Company.api'

export const companyApi = new CompanyApi({ instance: instance })

/**
 * query key 에 undefined 를 포함시키지 않기 위한 함수입니다.
 */
const isDefined = (v: unknown) => typeof v !== 'undefined'

/**
 * query-keys
 */

export const QUERY_KEY_COMPANY_API = {
  LIST: (variables?: Parameter<typeof companyApi.companyList>) =>
    ['COMPANY_LIST', variables].filter(isDefined),
}

/**
 * No description
 *
 * @tags company
 * @name CompanyList
 * @summary 직장 목록 조회
 * @request GET:/v1/company/
 * @secure    */

export const useCompanyListQuery = <
  TData = RequestFnReturn<typeof companyApi.companyList>,
>(
  params?: QueryHookParams<
    typeof companyApi.companyList,
    AxiosError<CommonErrorType>,
    TData
  >,
) => {
  const queryKey = QUERY_KEY_COMPANY_API.LIST(params?.variables)
  return useQuery({
    queryKey,
    queryFn: () => companyApi.companyList(params?.variables),
    ...params?.options,
  })
}

